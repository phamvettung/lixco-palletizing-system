CREATE TABLE products(
	product_code VARCHAR(50) PRIMARY KEY,
	product_name NVARCHAR(100) NOT NULL,
	net_weight FLOAT NOT NULL,
	num_bins_on_pallet INT NOT NULL,
	model INT NOT NULL,
	packet_weight FLOAT NOT NULL,
	num_packets_on_bin INT NOT NULL,
	product_images NVARCHAR NOT NULL
)

CREATE TABLE pallets
(
	pallet_id INT IDENTITY (1, 1) PRIMARY KEY,
	date_time DATETIME NOT NULL,
	product_code VARCHAR(50) NOT NULL,
	lever_code VARCHAR(50) NOT NULL,
	shift_id INT,
	team_id INT,
	line_id INT,
	reached BIT,
	craft BIT,
	num_order INT,
	lot VARCHAR(20),
	group_code NVARCHAR(100),
	form_name NVARCHAR(50),
	FOREIGN KEY (product_code) REFERENCES products (product_code) ON DELETE CASCADE ON UPDATE CASCADE
)



